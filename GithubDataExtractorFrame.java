    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String firstName = jTextField1.getText();
        String lastName = jTextField2.getText();
        String location = jTextField3.getText();
       
        if(firstName.length()==0||lastName.length()==0)
        {
            JOptionPane.showMessageDialog(null, "Fields marked with * are required");
        }
        else
        {
            User user = new User(firstName,lastName,location);
            listOfUsers.add(user);
            model.addRow(new Object[]{sNo++,firstName,lastName,location});
           // clearInputArea();
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(listOfUsers.size()==0)
        {
            JOptionPane.showMessageDialog(null,"list is empty");
        }
        else
        {
            new GithubApiController(listOfUsers).setVisible(true);
            this.setVisible(false);
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

